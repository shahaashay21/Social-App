    @Override
    public void onTextChanged(CharSequence s, int start, int before, int count) {
        setDefaultPinBackground(mPinFirstDigitEditText);
        setDefaultPinBackground(mPinSecondDigitEditText);
        setDefaultPinBackground(mPinThirdDigitEditText);
        setDefaultPinBackground(mPinForthDigitEditText);
        setDefaultPinBackground(mPinFifthDigitEditText);

        if (s.length() == 0) {
            setFocusedPinBackground(mPinFirstDigitEditText);
            mPinFirstDigitEditText.setText("");
        } else if (s.length() == 1) {
            setFocusedPinBackground(mPinSecondDigitEditText);
            mPinFirstDigitEditText.setText(s.charAt(0) + "");
            mPinSecondDigitEditText.setText("");
            mPinThirdDigitEditText.setText("");
            mPinForthDigitEditText.setText("");
            mPinFifthDigitEditText.setText("");
        } else if (s.length() == 2) {
            setFocusedPinBackground(mPinThirdDigitEditText);
            mPinSecondDigitEditText.setText(s.charAt(1) + "");
            mPinThirdDigitEditText.setText("");
            mPinForthDigitEditText.setText("");
            mPinFifthDigitEditText.setText("");
        } else if (s.length() == 3) {
            setFocusedPinBackground(mPinForthDigitEditText);
            mPinThirdDigitEditText.setText(s.charAt(2) + "");
            mPinForthDigitEditText.setText("");
            mPinFifthDigitEditText.setText("");
        } else if (s.length() == 4) {
            setFocusedPinBackground(mPinFifthDigitEditText);
            mPinForthDigitEditText.setText(s.charAt(3) + "");
            mPinFifthDigitEditText.setText("");
        } else if (s.length() == 5) {
            setDefaultPinBackground(mPinFifthDigitEditText);
            mPinFifthDigitEditText.setText(s.charAt(4) + "");

            hideSoftKeyboard(mPinFifthDigitEditText);
            sendConfirmationPin();
        }
    }

    public void sendConfirmationPin(){
        HttpRequest request = new HttpRequest(getApplicationContext());
        Map<String, String> sendData = new HashMap<>();
        sendData.put("email", emailAddress);
        sendData.put("token", mPinHiddenEditText.getText().toString());

        mPinFirstDigitEditText.setText("");
        mPinSecondDigitEditText.setText("");
        mPinThirdDigitEditText.setText("");
        mPinForthDigitEditText.setText("");
        mPinFifthDigitEditText.setText("");
        mPinHiddenEditText.setText("");

        request.sendPostRequest(IP + CONFIRMATION_ROUTE, sendData, new CallbackInterface() {
            @Override
            public void onCallBackComplete(String[] response) {
                if (response[0].toString().equals("success")) {
                    if (response[1].toString().equals("success")) {
                        Toast.makeText(getApplicationContext(), "Email and pin have been verified", Toast.LENGTH_LONG).show();
                        new Handler().postDelayed(new Runnable() {
                            @Override
                            public void run() {
                                Intent forwardMainActivity = new Intent(getApplicationContext(), LoginActivity.class);
                                startActivity(forwardMainActivity);
                                finish();
                            }
                        }, 2000);
                    }else if(response[1].toString().equals("verified")){
                        Toast.makeText(getApplicationContext(), "Already verified", Toast.LENGTH_LONG).show();
                        new Handler().postDelayed(new Runnable() {
                            @Override
                            public void run() {
                                Intent forwardMainActivity = new Intent(getApplicationContext(), LoginActivity.class);
                                startActivity(forwardMainActivity);
                                finish();
                            }
                        }, 2000);
                    }else {
                        Toast.makeText(getApplicationContext(), "Wrong Pin", Toast.LENGTH_SHORT).show();
                    }
                }else {
                    Toast.makeText(getApplicationContext(), "Something went wrong", Toast.LENGTH_SHORT).show();
                }
            }
        });
    }

