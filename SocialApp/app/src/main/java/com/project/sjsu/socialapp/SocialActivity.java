    /**
     * A {@link FragmentPagerAdapter} that returns a fragment corresponding to
     * one of the sections/tabs/pages.
     */
    public class SectionsPagerAdapter extends FragmentPagerAdapter {

        public SectionsPagerAdapter(FragmentManager fm) {
            super(fm);
        }

        @Override
        public Fragment getItem(int position) {
            switch (position) {
                case 0:
                    Front tab1 = new Front();
                    return tab1;
                case 1:
                    Messages tab2 = new Messages();
                    return tab2;
                case 2:
                    Profiles tab3 = new Profiles();
                    return tab3;
                case 3:
                    Friends tab4 = new Friends();
                    return tab4;
                case 4:
                    Settings tab5 = new Settings();
                    return tab5;
                default:
                    return null;
            }
        }

        @Override
        public int getCount() {
            // Show 5 total pages.
            return 5;
        }

        @Override
        public CharSequence getPageTitle(int position) {
            switch (position) {
                case 0:
                    return "Front";
                case 1:
                    return "Mail";
                case 2:
                    return "Profiles";
                case 3:
                    return "Friends";
                case 4:
                    return "Settings";
            }
            return null;
        }
    }
